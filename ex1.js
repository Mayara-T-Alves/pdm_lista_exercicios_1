function soma(num1, num2){
    return num1 + num2;
}
function sub(num1, num2){
    return num1 - num2;
}
function div(num1, num2){
    return num1 / num2;
}
function multplic(num1, num2){
    return num1 * num2;
}

console.log(soma(5, 10))
console.log(sub(4, 10))
console.log(div(9, 10))
console.log(multplic(2, 10))